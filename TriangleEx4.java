package Crash_Triangles.src;

public class TriangleEx4 {
    public static void main(String[] args) {
        for (int s = 5; s >= 1; s --) {
            for (int st = 1; st <= s; st++) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
